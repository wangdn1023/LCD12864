u8 LinearSearch(u16 *Array,u16 index,u8 length)
{
	int i=0;
	u8 result;
		while(1){
			if(index<Array[0])
			{
				index=0;
				break;
			}
			if(index>=Array[length-1])
			{
				result=100;
				break;
			}
			if(index>=Array[i]&&index<Array[i+1])
			{				
				result=(index-Array[i])*10/(Array[i+1]-Array[i])+i*10;
				break;
			}
			else
				i++;
		}		
			return result;	
}

